void Sound_Init(void){
  NVIC_ST_CTRL_R = 0;       // Turn off SysTick during initialization
  NVIC_ST_RELOAD_R = 80000000/11000;  // ~11kHz
  NVIC_ST_CURRENT_R = 0;    // Clear current register to load new reload value
  NVIC_SYS_PRI3_R = (NVIC_SYS_PRI3_R & 0x00FFFFFF) | 0x40000000;  // Priority 2
  NVIC_ST_CTRL_R = 0x7;     // Turn on the Timer with external clock and interrupts (bit2 ClkSrc, bit1 IntEn, bit0 Enable
  
  DAC_Init();   // Setup DAC output
};
static uint32_t idx;
static uint8_t flag;
static uint32_t num;
static const uint8_t *WavPt;

// Interrupt service routine
// Executed every 12.5ns*(period)
void SysTick_Handler(void){
  if (flag){
    DAC_Out(WavPt[idx]);              // Write current position to DAC
    idx += 1;
    if (idx >= num){
      flag = 0;
    }
    GPIO_PORTF_DATA_R ^= 0x2;  // Toggle Red LED
  }
}


